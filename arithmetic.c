#include<stdio.h>
 int main()
{
    /*
DIVIDE OPERATER(/)
3/4 = o (int/int = 0)
3.0/4 = 0.75 (real/int = real)
3/4.0 = 0.75 (int/real = real)

    int x;
    x=3/4;
    printf("%d \n",x);
    */

    /*
    MODULO OPEROTER(%)
    3%4 = 3  (gives remainder)
    */
    int y;
    y=3%4;
    printf("%d",y);
    return 0;
}

